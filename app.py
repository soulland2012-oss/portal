def _parse_date(val):
    if val is None:
        return None
    if hasattr(val, 'strftime'):
        return val.strftime('%Y-%m-%d')
    s = str(val).strip()
    for fmt in ('%m/%d/%Y', '%Y-%m-%d', '%d.%m.%Y', '%d/%m/%Y'):
        try:
            return datetime.strptime(s, fmt).strftime('%Y-%m-%d')
        except ValueError:
            pass
    return None


def _leaf_status(status_raw, finish, today):
    mapped = _JIRA_STATUS.get(status_raw)
    if mapped:
        return mapped
    if finish and finish < today:
        return 'overdue'
    return 'pending'


