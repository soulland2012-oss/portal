    ws = wb.active
    headers = [c.value for c in next(ws.iter_rows(max_row=1))]
    col = {h: i for i, h in enumerate(headers)}

    today = datetime.now().strftime('%Y-%m-%d')

    by_epic = {e: [] for e in _EPIC_ORDER}

    for row in ws.iter_rows(min_row=2, values_only=True):
        key = row[col['KEY']]
        if not key or not str(key).startswith('PMOAI-'):
            continue
        itype = str(row[col['ISSUE_TYPE']] or '').strip()
        if itype == 'Epic':
            continue

        name      = str(row[col['SUMMARY']] or '').strip()
        status_raw= str(row[col['STATUS']] or '').strip()
        performer = str(row[col['PERFORMER']] or '').strip()
        start     = _parse_date(row[col['START_DATE']])
        end_raw   = row[col['END_DATE']] or row[col['DUE_DATE']]
        finish    = _parse_date(end_raw)
        num       = int(str(key).replace('PMOAI-', ''))

        epic = _EPIC_MAP.get(_norm(name))
        if epic:
            by_epic[epic].append({
                'num': num, 'name': name, 'status_raw': status_raw,
                'performer': performer, 'start': start, 'finish': finish,
            })

    wb.close()

    tasks = []
    uid_seq = [0]
    def nuid(): uid_seq[0] += 1; return str(uid_seq[0])

    total = done_c = active_c = overdue_c = pending_c = 0
    all_starts, all_finishes = [], []

    for ei, epic_name in enumerate(_EPIC_ORDER, 1):
        wbs_e = f'1.{ei}'
        epic_tasks = sorted(by_epic[epic_name], key=lambda r: r['num'])

        e_done = e_active = e_overdue = e_pending = 0
        e_starts, e_finishes = [], []
        leaf_tasks = []

        for ti, r in enumerate(epic_tasks, 1):
            st  = _leaf_status(r['status_raw'], r['finish'], today)
            pct = 100 if st == 'done' else 0

            if st == 'done':      e_done    += 1; done_c    += 1
            elif st == 'active':  e_active  += 1; active_c  += 1
            elif st == 'overdue': e_overdue += 1; overdue_c += 1
            else:                 e_pending += 1; pending_c += 1
            total += 1

            if r['start']:  e_starts.append(r['start'])
            if r['finish']: e_finishes.append(r['finish'])

            res = [r['performer']] if r['performer'] else []
            leaf_tasks.append({
                'uid': nuid(), 'id': f'PMOAI-{r["num"]}', 'name': r['name'],
                'wbs': f'{wbs_e}.{ti}', 'level': 3,
                'summary': False, 'milestone': False, 'critical': False,
                'start': r['start'], 'finish': r['finish'],
                'pct': pct, 'resources': res, 'status': st,
                'pres': False, 'text10': '',
            })

        e_total = e_done + e_active + e_overdue + e_pending
        e_pct   = round(e_done / e_total * 100) if e_total > 0 else 0
        e_start = min(e_starts)  if e_starts  else None
        e_finish= max(e_finishes)if e_finishes else None
        if e_start:  all_starts.append(e_start)
        if e_finish: all_finishes.append(e_finish)

        e_st = ('done'   if e_pct == 100
                else 'active'  if e_active > 0 or e_pct > 0
                else 'pending')
        tasks.append({
            'uid': nuid(), 'id': f'PMOAI-{ei}', 'name': epic_name,
            'wbs': wbs_e, 'level': 2,
            'summary': True, 'milestone': False, 'critical': False,
            'start': e_start, 'finish': e_finish,
            'pct': e_pct, 'resources': [], 'status': e_st,
            'pres': False, 'text10': '',
        })
        tasks.extend(leaf_tasks)

    overall_pct  = round(done_c / total * 100) if total > 0 else 0
    root_start   = min(all_starts)  if all_starts  else None
    root_finish  = max(all_finishes)if all_finishes else None

    tasks.insert(0, {
        'uid': nuid(), 'id': 'PMOAI-0', 'name': 'AI ТРАНСФОРМАЦИЯ SQB',
        'wbs': '1', 'level': 1,
        'summary': True, 'milestone': False, 'critical': False,
        'start': root_start, 'finish': root_finish,
        'pct': overall_pct, 'resources': [], 'status': 'active',
        'pres': False, 'text10': '',
    })

    saved = datetime.fromtimestamp(os.path.getmtime(EXCEL_FILE)).strftime('%Y-%m-%d')

    _cache = {
        'meta': {
            'name': 'AI Трансформация SQB',
            'start': root_start, 'finish': root_finish,
            'saved': saved, 'today': today,
        },
        'tasks': tasks,
        'stats': {
            'total': total, 'done': done_c, 'active': active_c,
            'pending': pending_c, 'overdue': overdue_c,
        },
        'resources': [],
    }
    return _cache


